import heapq


class Solution(object):
    def shortestPath(self, grid, k):
        """
        :type grid: List[List[int]]
        :type k: int
        :rtype: int
        """

        '''
        need to track steps, obstacles-remaining, (x,y),
        if we want to get the shortest path, by eliminating k obstacles, 
            order the entry in pq by number of steps first
        '''
        rows = len(grid)
        cols = len(grid[0])
        obstacles_remaining_matrix = [[float('-inf')] * cols for _ in range(rows)]
        obstacles_remaining_matrix[0][0] = k

        # initial element in pq, to start of bfs search
        pq = [(0, k, 0, 0)]  # steps, obstacles-remaining, x, y

        x_limit = rows - 1
        y_limit = cols - 1
        while pq:
            steps, obstacles_remaining, r, c = heapq.heappop(pq)

            next_steps = [(r + 1, c), (r - 1, c), (r, c + 1), (r, c - 1)]
            for nr, nc in next_steps:

                if nr > x_limit or nr < 0 or nc > y_limit or nc < 0:
                    continue

                if nr == x_limit and nc == y_limit:
                    print("reached end state")
                    return steps+1

                n_obstacles_remaining = obstacles_remaining - grid[nr][nc]

                # we cannot go in debt to remove obstacles
                if n_obstacles_remaining < 0:
                    continue

                n_steps = steps + 1

                """
                NO OPTIMIZATION WITH STEPS REQUIRED, BECAUSE ANYWAYS WE ARE OPTIMIZING FOR CHOOSING SMALLEST STEPS
                FIRST DUE TO THE ENTRY INTO PQ, AND DOING HEAPPOP TO SELECT OPTION WITH SMALLEST STEPS TAKEN FIRST
                
                WE NEED TO OPTIMIZE FOR THE GREATEST NUMBER OF OBSTACLES REMAINING AT EACH CELL
                ONLY UPDATE A CELL IN VISITED, AND PUT NEW ENTRY ONTO PQ IF OBSTACLES REMAINING GREATER THAN PREVIOUS 
                """
                if n_obstacles_remaining >= obstacles_remaining_matrix[nr][nc]:
                    obstacles_remaining_matrix[nr][nc] = n_obstacles_remaining
                    heapq.heappush(pq, (n_steps, n_obstacles_remaining, nr, nc))

        if obstacles_remaining_matrix[x_limit][y_limit] == float("inf"):
            return -1
        return obstacles_remaining_matrix[x_limit][y_limit]
